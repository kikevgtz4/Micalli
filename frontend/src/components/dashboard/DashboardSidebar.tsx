"use client";
import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useAuth } from '@/contexts/AuthContext';

export default function DashboardSidebar() {
  const pathname = usePathname();
  const { user, logout } = useAuth();

  const navigation = [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'My Properties', href: '/dashboard/properties', icon: BuildingIcon },
    { name: 'Messages', href: '/dashboard/messages', icon: ChatIcon },
    { name: 'Viewing Requests', href: '/dashboard/viewing-requests', icon: CalendarIcon },
    { name: 'Analytics', href: '/dashboard/analytics', icon: ChartIcon },
    { name: 'Account Settings', href: '/dashboard/settings', icon: SettingsIcon },
  ];

  const isActive = (path: string) => {
    return pathname === path || pathname.startsWith(`${path}/`);
  };

  return (
    <div className="w-64 h-screen bg-white border-r border-gray-200 fixed">
      <div className="flex items-center justify-center h-16 px-4 border-b border-gray-200">
        <Link href="/" className="text-xl font-bold text-indigo-600">
          UniHousing
        </Link>
      </div>
      <div className="p-4">
        <div className="flex items-center space-x-3 mb-6">
          <div className="h-10 w-10 rounded-full bg-indigo-100 flex items-center justify-center text-indigo-600">
            {user?.username?.charAt(0).toUpperCase() || 'U'}
          </div>
          <div>
            <p className="font-medium">{user?.username || 'User'}</p>
            <p className="text-sm text-gray-500">Property Owner</p>
          </div>
        </div>

        <nav className="space-y-1">
          {navigation.map((item) => (
            <Link
              key={item.name}
              href={item.href}
              className={`flex items-center px-3 py-2 text-sm font-medium rounded-md ${
                isActive(item.href)
                  ? 'bg-indigo-50 text-indigo-600'
                  : 'text-gray-700 hover:bg-gray-50 hover:text-gray-900'
              }`}
            >
              <item.icon
                className={`mr-3 h-5 w-5 ${
                  isActive(item.href) ? 'text-indigo-500' : 'text-gray-400'
                }`}
              />
              {item.name}
            </Link>
          ))}
        </nav>
      </div>
      <div className="absolute bottom-0 w-full p-4 border-t border-gray-200">
        <button
          onClick={logout}
          className="flex items-center px-3 py-2 text-sm font-medium text-red-600 rounded-md hover:bg-red-50 w-full"
        >
          <LogoutIcon className="mr-3 h-5 w-5 text-red-500" />
          Sign out
        </button>
      </div>
    </div>
  );
}

